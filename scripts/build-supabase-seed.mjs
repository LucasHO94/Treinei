// Gera o seed SQL do catálogo NATIVO (grupos musculares, exercícios, alimentos e receitas)
// para o Supabase, a partir dos JSONs em public/data/*.json.
//
// Por que é necessário: as FKs do banco (workout_exercises.exercise_id → exercises,
// meal_items.food_id → foods, recipe_items.food_id → foods) exigem que o catálogo nativo
// exista no Postgres ANTES de qualquer dado do usuário sincronizar. O app lê o catálogo do
// JSON local (offline-first); o servidor só precisa das linhas base para integridade de FK.
//
// Uso: node scripts/build-supabase-seed.mjs
// Saída: supabase/seed/catalog_full.sql  (rodar no SQL Editor do Supabase, uma vez)

import { readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const __dirname = dirname(fileURLToPath(import.meta.url))
const dataDir = join(__dirname, '..', 'public', 'data')
const outFile = join(__dirname, '..', 'supabase', 'seed', 'catalog_full.sql')

const exercisesFile = JSON.parse(readFileSync(join(dataDir, 'exercises.json'), 'utf8'))
const foodsFile = JSON.parse(readFileSync(join(dataDir, 'foods.json'), 'utf8'))
const recipesFile = JSON.parse(readFileSync(join(dataDir, 'recipes.json'), 'utf8'))

// ---- Helpers de literais SQL ----
const S = (v) => (v == null ? 'null' : `'${String(v).replace(/'/g, "''")}'`)
const N = (v) => (v == null || v === '' ? 'null' : Number(v))
const B = (v) => (v ? 'true' : 'false')

/** Emite inserts em lotes com ON CONFLICT DO NOTHING (idempotente / re-executável). */
function insertBatched(table, columns, rows, toValues, chunk = 200) {
  const out = []
  for (let i = 0; i < rows.length; i += chunk) {
    const slice = rows.slice(i, i + chunk)
    const values = slice.map((r) => `  (${toValues(r).join(', ')})`).join(',\n')
    out.push(
      `insert into ${table} (${columns.join(', ')}) values\n${values}\non conflict (id) do nothing;\n`,
    )
  }
  return out.join('\n')
}

const parts = []
parts.push('-- Treinei — seed COMPLETO do catálogo nativo (gerado por scripts/build-supabase-seed.mjs).')
parts.push('-- Rodar no SQL Editor do Supabase DEPOIS das migrations 0001–0004. Idempotente.')
parts.push('-- Ordem respeita as FKs: muscle_groups → exercises → foods → recipes → recipe_items.\n')
parts.push('begin;\n')

// muscle_groups (id é "generated by default as identity" → insert explícito é permitido)
parts.push('-- Grupos musculares')
parts.push(
  insertBatched(
    'muscle_groups',
    ['id', 'slug', 'name', 'sort_order', 'image_url'],
    exercisesFile.muscleGroups,
    (g) => [N(g.id), S(g.slug), S(g.name), N(g.sort_order), S(g.image_url)],
  ),
)
parts.push(
  "select setval(pg_get_serial_sequence('muscle_groups', 'id'), (select max(id) from muscle_groups));\n",
)

// exercises (só colunas base do schema; metadados V2 vivem no JSON local)
parts.push('-- Exercícios (catálogo nativo)')
parts.push(
  insertBatched(
    'exercises',
    ['id', 'muscle_group_id', 'name', 'media_url', 'is_custom', 'owner_id', 'created_at'],
    exercisesFile.exercises,
    (e) => [S(e.id), N(e.muscle_group_id), S(e.name), S(e.media_url), B(e.is_custom), 'null', S(e.created_at)],
  ),
)

// foods (colunas base)
parts.push('-- Alimentos (catálogo TACO nativo)')
parts.push(
  insertBatched(
    'foods',
    ['id', 'name', 'portion_desc', 'portion_grams', 'protein_g', 'carbs_g', 'fat_g', 'kcal', 'is_custom', 'owner_id'],
    foodsFile.foods,
    (f) => [
      S(f.id),
      S(f.name),
      S(f.portion_desc),
      N(f.portion_grams),
      N(f.protein_g),
      N(f.carbs_g),
      N(f.fat_g),
      N(f.kcal),
      B(f.is_custom),
      'null',
    ],
  ),
)

// recipes + recipe_items (colunas base)
parts.push('-- Receitas curadas (nativas)')
parts.push(
  insertBatched(
    'recipes',
    ['id', 'name', 'meal_kind', 'description', 'image_url', 'servings', 'prep_minutes', 'source', 'is_custom', 'owner_id', 'created_at'],
    recipesFile.recipes,
    (r) => [
      S(r.id),
      S(r.name),
      S(r.meal_kind),
      S(r.description),
      S(r.image_url),
      N(r.servings),
      N(r.prep_minutes),
      S(r.source),
      B(r.is_custom),
      'null',
      S(r.created_at),
    ],
  ),
)
parts.push('-- Itens das receitas')
parts.push(
  insertBatched(
    'recipe_items',
    ['id', 'recipe_id', 'food_id', 'quantity'],
    recipesFile.recipeItems,
    (i) => [S(i.id), S(i.recipe_id), S(i.food_id), N(i.quantity)],
  ),
)

parts.push('commit;')

writeFileSync(outFile, parts.join('\n'), 'utf8')

const kb = (Buffer.byteLength(parts.join('\n'), 'utf8') / 1024).toFixed(0)
console.log(`✓ Seed gerado: supabase/seed/catalog_full.sql (${kb} KB)`)
console.log(
  `  ${exercisesFile.muscleGroups.length} grupos · ${exercisesFile.exercises.length} exercícios · ` +
    `${foodsFile.foods.length} alimentos · ${recipesFile.recipes.length} receitas · ${recipesFile.recipeItems.length} itens`,
)
